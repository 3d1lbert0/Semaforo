void Display_init(){
    stdio_init_all();   // Inicializa os tipos stdio padrão presentes ligados ao binário

    // Inicialização do i2c
    i2c_init(i2c1, 3000 * 1000);
    gpio_set_function(I2C_SDA, GPIO_FUNC_I2C);
    gpio_set_function(I2C_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(I2C_SDA);
    gpio_pull_up(I2C_SCL);

    // Processo de inicialização completo do OLED SSD1306
    ssd1306_init();

    // Configuração do frame_area
    frame_area.start_column = 0;
    frame_area.end_column = ssd1306_width - 1;
    frame_area.start_page = 0;
    frame_area.end_page = ssd1306_n_pages - 1;

    calculate_render_area_buffer_length(&frame_area);
}

void clearDisplay(){
    // zera o display inteiro
    memset(ssd, 0, ssd1306_buffer_length);
    render_on_display(ssd, &frame_area);
}

void display_aligned_text(uint8_t *buffer, char *text, int y, int alignment) {
    int text_width = strlen(text) * 8;
    int x;

    switch (alignment) {
        case 0: // Alinhar à esquerda
            x = 0;
            break;
        case 1: // Centralizar
            x = (ssd1306_width - text_width) / 2;
            break;
        case 2: // Alinhar à direita
            x = ssd1306_width - text_width;
            break;
        default: // Padrão: alinhar à esquerda
            x = 0;
            break;
    }

    // Desenha o texto no buffer na posição calculada
    ssd1306_draw_string(ssd, x, y, text);

    // Renderiza o buffer no display
    render_on_display(ssd, &frame_area);
}


void texto(char *line1, char *line2, char *line3, char *line4) {
    clearDisplay(); // Limpa o display antes de renderizar
    display_aligned_text(ssd, line1, 0, 1);  // Linha 1 centralizada
    display_aligned_text(ssd, line2, 16, 1); // Linha 2 centralizada
    display_aligned_text(ssd, line3, 32, 1); // Linha 3 centralizada
    display_aligned_text(ssd, line4, 48, 1); // Linha 4 centralizada
}

